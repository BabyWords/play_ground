package com.wujunwen.springbootplay.lambda;

public class Apple {
  String color;
  Integer weight;

  public Apple(String color, int weight) {
    this.color = color;
    this.weight = weight;
  }

  public String getColor() {
    return color;
  }

  public void setColor(String color) {
    this.color = color;
  }

  public int getWeight() {
    return weight;
  }

  public void setWeight(int weight) {
    this.weight = weight;
  }


}